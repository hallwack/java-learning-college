public class PercobaanDua {
  public static void main(String[] args) {
    double pi, r, area;

    // Jari-jari lingkaran
    r = 10.8;

    // Phi
    pi = 3.1416;

    // Hitung luas lingkaran
    area = pi*r*r;

    System.out.println("Luas lingkaran berjari-jari "+ r +" adalah "+ area);
  }
}
