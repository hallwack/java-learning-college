public class PercobaanEmpat {
  public static void main(String[] args) {
    char ch1, ch2, ch3;

    // Kode untuk 'X'
    ch1 = 88;
    ch2 = 'Y';

    System.out.println("ch1 dan ch2 : "+ ch1 + " "+ ch2);

    System.out.println();

    // Char diperlakukan sebagai bilangan bulat
    ch3 = 'X';
    System.out.println("ch3 berisi"+ ch3);

    // Menaikkan nilai ch3;
    System.out.println("ch3 sekarang bernilai "+ ch3);
  }
}
